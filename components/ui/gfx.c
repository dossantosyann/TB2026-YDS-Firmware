void gfx_draw_text(int x, int y, const char *s, gfx_color_t color, int scale)
{
    if (scale < 1) scale = 1;
    for (; *s; s++) {
        unsigned char ch = (unsigned char)*s;
        if (ch >= 0x20 && ch <= 0x7E) {
            const uint8_t *g = s_font[ch - 0x20];
            for (int col = 0; col < GFX_FONT_W; col++)
                for (int row = 0; row < GFX_FONT_H; row++)
                    if ((g[col] >> row) & 1)
                        gfx_fill_rect(x + col * scale, y + row * scale, scale, scale, color);
        }
        x += GFX_CHAR_W * scale;     /* 5px glyph + 1px gap, scaled */
    }
}

void gfx_blit_1bpp(int x, int y, int w, int h, const uint8_t *bitmap, gfx_color_t color)
{
    const int stride = (w + 7) / 8;     /* bytes per row, matching icon2c.py */
    for (int row = 0; row < h; row++) {
        const uint8_t *line = &bitmap[row * stride];
        for (int col = 0; col < w; col++)
            if ((line[col >> 3] >> (7 - (col & 7))) & 1)
                gfx_pixel(x + col, y + row, color);     /* gfx_pixel clips to the canvas */
    }
}

void gfx_flush(void)
{
    /* The framebuffer is native RGB565; display_oled_draw byte-swaps each pixel into
       its own internal DMA-capable line buffer, so PSRAM never feeds the SPI DMA. */
    display_oled_draw((const uint8_t *)s_fb, sizeof s_fb);
}
